package com.celestial.meek.realTest_2016_10;

public abstract class Rectangle {
	protected int width;
	
	protected int length;

	public int getWidth() {
		return width;
	}
	
	public int getLength() {
		return length;
	} 

	public void setLength(int length) {
		this.length = length;
	}
	
	public void setWidth(int width) {
		this.width = width;
	}
	
	
}
