package com.celestial.meek.realTest_2017_04;

public class TradeDependence {
	
	Trader buyer;
	Money money;
	
	Trader seller;
	Goods goods;
	
	Trader manager;
	Fee fee;
	
	
	
	public Trader getBuyer() {
		return buyer;
	}
	public void setBuyer(Trader buyer) {
		this.buyer = buyer;
	}
	public Money getMoney() {
		return money;
	}
	public void setMoney(Money money) {
		this.money = money;
	}
	public Trader getSeller() {
		return seller;
	}
	public void setSeller(Trader seller) {
		this.seller = seller;
	}
	public Goods getGoods() {
		return goods;
	}
	public void setGoods(Goods goods) {
		this.goods = goods;
	}
	public Trader getManager() {
		return manager;
	}
	public void setManager(Trader manager) {
		this.manager = manager;
	}
	public Fee getFee() {
		return fee;
	}
	public void setFee(Fee fee) {
		this.fee = fee;
	}
	
	
	
	
}



